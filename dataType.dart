void main() {
  //Data Types
  
  //Strings
  String myCode = "Hello World";
  print("String: $myCode");
  
  //Integers
  int math = 10;
  print("Integer: $math");
  
  //Doubles
  double decimal = 3.14;
  print("Double: $decimal");
    
  //Booleans
  bool status = false;
  print("Boolean: $status"); 
  
  //Dynamic
  dynamic fullName = "John Walker";
  print("Dynamic: $fullName");
  
}
