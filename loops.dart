void main() {
  // For Loop
  for (int i = 1; i <= 5; i++) {
    print(i);
  }
  
  //For In Loop
  var names = ["John", "Rick", "Tom", "Jerry", "Donald"];
  print(names);
  for (var name in names){
    print(name);
  }
  
  //While Loop
  var num = 5;
  while(num >=1){
    print(num);
    num--;
  }
}
