void main() {
  //Logics
  
  //If
  var number = 5;
  if(number == 5){
    print("The number is 5!");
  }
  
  //If-else
  if(number != 5){
    print("The number is not 5!");
  }else{
    print("Hello world, The number is 5!");
  }
  
  //If Else If
  var num = 10;
  if(num==3){
    print("The number is 3!");
  }else if(num==10){
    print("Hello World");
  }
}
